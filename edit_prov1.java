    private void UpdateVehicleInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateVehicleInfoActionPerformed

        String query2, size;
        int result2;

        if (small.isSelected()) {
            size = "Small";
        } else {
            size = "Large";
        }

        if (Plate.getText().equalsIgnoreCase("") || Make.getText().equalsIgnoreCase("") || !(small.isSelected() || Large.isSelected())) {
            JOptionPane.showMessageDialog(null, "Make sure you have filled all required fields.", "Entry Error", JOptionPane.ERROR_MESSAGE);

        } else {

            connectionClass.getConnection();
            query2 = "update vehicle set make = '" + Make.getText() + "', platenumber = '" + Plate.getText() + "', size = '" + size + "' where LicenseNumber = '" + lnum.getText() + "'";
            result2 = connectionClass.getResrultsetupdate(query2);

            if (result2 > 0 ) {
                JOptionPane.showMessageDialog(null, "Changed succesfully", "Success!", JOptionPane.OK_OPTION);
                setVisible(false);
                ProviderHomePage_2 ProviderHomePage_2 = new ProviderHomePage_2();
                ProviderHomePage_2.setVisible(true);
            }
        }

    }//GEN-LAST:event_UpdateVehicleInfoActionPerformed

