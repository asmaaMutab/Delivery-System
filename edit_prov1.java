    private void UpdatePersonalInfo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdatePersonalInfo1ActionPerformed
        String query1;
        int result1;

        if (FN.getText().equalsIgnoreCase("") || LN.getText().equalsIgnoreCase("") || Pass.getText().equalsIgnoreCase("") || Cpass.getText().equalsIgnoreCase("") || phone.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Make sure you have filled all required fields.", "Entry Error", JOptionPane.ERROR_MESSAGE);

        } else if (!(Arrays.equals(Pass.getPassword(), Cpass.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Password fields do not match, please try again.", "OOPS", JOptionPane.ERROR_MESSAGE);
        } else {

            connectionClass.getConnection();

            query1 = "update account set firstname='" + FN.getText() + "',lastname='" + LN.getText() + "',password='" + Pass.getPassword().toString() + "', phonenumber= '" + phone.getText() + "' where email='" + email.getText() + "'";
            result1 = connectionClass.getResrultsetupdate(query1);

            if (result1 > 0) {
                JOptionPane.showMessageDialog(null, "Changed succesfully", "Success!", JOptionPane.OK_OPTION);
                setVisible(false);
                ProviderHomePage_2 ProviderHomePage_2 = new ProviderHomePage_2();
                ProviderHomePage_2.setVisible(true);
            }
        }
    }//GEN-LAST:event_UpdatePersonalInfo1ActionPerformed

